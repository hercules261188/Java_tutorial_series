public class ThirdJava {
    public static void main(String[] args) {
        //Abbreviating Operators
        int res = 10 + 20;
        System.out.println("Sum1 = " + res);

        res++;
        System.out.println("Sum2 = " + res);

        res--;
        System.out.println("Dec3 = " + res);

        res += 30;
        System.out.println("Sum3 = " + res);

        res *= 10;
        System.out.println("Multiply = " + res);

        res /= 2;
        System.out.println("Division = " + res);

        boolean isValid = false;
        if(isValid == false){
            System.out.println("This is valid if Block");
        }
    }
}
